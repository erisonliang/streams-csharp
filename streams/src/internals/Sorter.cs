namespace hhlogic.streams.internals {
//----------------------------------------------------------------------------------------------------------------------
using System;
using System.Collections.Generic;
//======================================================================================================================


//======================================================================================================================
public sealed class Sorter<T> : IComparer<T>
{
  //--------------------------------------------------------------------------------------------------------------------
  private Func<T, T, int> func;
  //--------------------------------------------------------------------------------------------------------------------
  public Sorter(Func<T, T, int> f)
  {
    this.func = f;
  }
  //--------------------------------------------------------------------------------------------------------------------
  public int Compare(T a, T b)
  {
    return func(a, b);
  }
  //--------------------------------------------------------------------------------------------------------------------
}
//======================================================================================================================


//======================================================================================================================
} // End Namespace
